function graduates (students) {
  var studentObj= {};
  for(z = 0; z < students.length; z ++){
  if(studentObj[students[z].class] === undefined){
    studentObj[students[z].class] = []
      }if(students[z].score > 75){
        studentObj[students[z].class].push({
            name:students[z].name,
              score: students[z].score
        });
      }
  }
 
  return studentObj
  // Code disini
}

